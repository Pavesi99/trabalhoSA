
package br.senai.sc.trabalhoFinalSA.modelo;

public class Equipe {
    
    private int codEqu;
    private String nomEqu;
    private String desEqu;

    public int getCodEqu() {
        return codEqu;
    }

    public void setCodEqu(int codEqu) {
        this.codEqu = codEqu;
    }

    public String getNomEqu() {
        return nomEqu;
    }

    public void setNomEqu(String nomEqu) {
        this.nomEqu = nomEqu;
    }

    public String getDesEqu() {
        return desEqu;
    }

    public void setDesEqu(String desEqu) {
        this.desEqu = desEqu;
    }
    
    
    
}
