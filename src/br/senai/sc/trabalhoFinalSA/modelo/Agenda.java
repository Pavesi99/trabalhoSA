
package br.senai.sc.trabalhoFinalSA.modelo;

public class Agenda {
    
    private String criAge;
    private String comAge;
    private String titAge;
    private String desAge;

    public String getCriAge() {
        return criAge;
    }

    public void setCriAge(String criAge) {
        this.criAge = criAge;
    }

    public String getComAge() {
        return comAge;
    }

    public void setComAge(String comAge) {
        this.comAge = comAge;
    }

    public String getTitAge() {
        return titAge;
    }

    public void setTitAge(String titAge) {
        this.titAge = titAge;
    }

    public String getDesAge() {
        return desAge;
    }

    public void setDesAge(String desAge) {
        this.desAge = desAge;
    }

   
    
}
